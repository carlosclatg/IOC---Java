    private void exyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exyActionPerformed
        // TODO add your handling code here:
        if(this.numeroprevi == -1){ //No hi ha numero previ guardat
            if(!pantallaResultat.getText().toString().isEmpty()){
                this.numeroprevi = Integer.parseInt(pantallaResultat.getText().toString());
                escriu = true;
                exy.setEnabled(false);
                ex2.setEnabled(false);
                botoigual.setEnabled(true);
                return;
                
            } else { 
                return;}
            
        } else {
                this.numeroprevi = Integer.parseInt(pantallaResultat.getText().toString());
                escriu = true;
                exy.setEnabled(false);
                ex2.setEnabled(false);
                botoigual.setEnabled(true);
                
        }
        
        
    }//GEN-LAST:event_exyActionPerformed

